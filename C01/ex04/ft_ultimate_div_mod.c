#include <stdio.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	div;
	int	rest;

	if (b != 0)
	{
		div = *a / *b;
		rest = *a % *b;
		*a = div;
		*b = rest;
	}
}
